def generate_phase_sensitive_ising(n_qubits, n_samples_per_class, seed=42):
    """
    Generate Ising states very close to the critical point, then apply
    class-dependent unitary rotations that preserve |Ïˆ_i|Â² structure
    while creating class-distinguishing quantum phases.
    
    Class +1: Ground states with h/J in [0.85, 1.0], 
              then Z-rotations on qubits 0,1
    Class -1: Ground states with h/J in [1.0, 1.15],
              then Z-rotations on qubits 2,3
    
    The Z-rotations modify phases but NOT measurement probabilities
    in the computational basis. This means |Ïˆ_i|Â² is nearly identical
    for both classes, but the quantum state vectors differ.
    """
    rng = np.random.RandomState(seed)
    
    states = []
    labels = []
    
    # Class +1: slightly below critical point + Z rotation on qubits 0,1
    h_vals = rng.uniform(0.85, 1.0, n_samples_per_class)
    for h in h_vals:
        psi, _ = get_ground_state(n_qubits, 1.0, h)
        # Apply Z-rotations (phase-only changes)
        angle1 = rng.uniform(0.8, 1.2)
        angle2 = rng.uniform(0.8, 1.2)
        psi = apply_local_rotation(psi, n_qubits, 
                                    [angle1, angle2], ['z', 'z'], [0, 1])
        # Small noise
        noise = rng.normal(0, 0.01, size=psi.shape) + \
                1j * rng.normal(0, 0.01, size=psi.shape)
        psi = psi + noise
        psi = psi / np.linalg.norm(psi)
        states.append(psi)
        labels.append(+1)
    
    # Class -1: slightly above critical point + Z rotation on qubits 2,3
    h_vals = rng.uniform(1.0, 1.15, n_samples_per_class)
    for h in h_vals:
        psi, _ = get_ground_state(n_qubits, 1.0, h)
        # Apply Z-rotations on DIFFERENT qubits
        angle1 = rng.uniform(2.0, 2.5)
        angle2 = rng.uniform(2.0, 2.5)
        psi = apply_local_rotation(psi, n_qubits,
                                    [angle1, angle2], ['z', 'z'], [2, 3])
        noise = rng.normal(0, 0.01, size=psi.shape) + \
                1j * rng.normal(0, 0.01, size=psi.shape)
        psi = psi + noise
        psi = psi / np.linalg.norm(psi)
        states.append(psi)
        labels.append(-1)
    
    return states, np.array(labels)


