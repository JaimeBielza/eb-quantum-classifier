def quantum_fidelity(psi1, psi2):
    return np.abs(np.vdot(psi1, psi2)) ** 2

def construct_pair_state(psi_a, psi_b):
    pair = np.kron(psi_a, psi_b)
    return pair / np.linalg.norm(pair)

def random_unitary(dim, rng):
    """Generate a random unitary via QR decomposition of a random matrix."""
    Z = rng.randn(dim, dim) + 1j * rng.randn(dim, dim)
    Q, R = np.linalg.qr(Z)
    D = np.diag(np.diag(R) / np.abs(np.diag(R)))
    return Q @ D

def apply_local_rotation(psi, n_qubits, angles, axes, qubit_indices):
    """Apply single-qubit rotations to specific qubits."""
    state = psi.copy()
    dim = 2 ** n_qubits
    
    for angle, axis, qi in zip(angles, axes, qubit_indices):
        if axis == 'x':
            gate = np.array([[np.cos(angle/2), -1j*np.sin(angle/2)],
                           [-1j*np.sin(angle/2), np.cos(angle/2)]])
        elif axis == 'y':
            gate = np.array([[np.cos(angle/2), -np.sin(angle/2)],
                           [np.sin(angle/2), np.cos(angle/2)]])
        elif axis == 'z':
            gate = np.array([[np.exp(-1j*angle/2), 0],
                           [0, np.exp(1j*angle/2)]])
        
        # Build full operator
        full_gate = np.eye(1)
        for j in range(n_qubits):
            full_gate = np.kron(full_gate, gate if j == qi else np.eye(2))
        
        state = full_gate @ state
    
    return state / np.linalg.norm(state)


