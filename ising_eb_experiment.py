def pauli_x():
    return csr_matrix(np.array([[0, 1], [1, 0]], dtype=complex))

def pauli_y():
    return csr_matrix(np.array([[0, -1j], [1j, 0]], dtype=complex))

def pauli_z():
    return csr_matrix(np.array([[1, 0], [0, -1]], dtype=complex))

def identity(n=2):
    return eye(n, format='csr', dtype=complex)

def build_tfim_hamiltonian(n_qubits, J, h):
    dim = 2 ** n_qubits
    H = csr_matrix((dim, dim), dtype=complex)
    for i in range(n_qubits - 1):
        term = identity(1)
        for j in range(n_qubits):
            op = pauli_z() if (j == i or j == i + 1) else identity()
            term = op if j == 0 else kron(term, op, format='csr')
        H += -J * term
    for i in range(n_qubits):
        term = identity(1)
        for j in range(n_qubits):
            op = pauli_x() if j == i else identity()
            term = op if j == 0 else kron(term, op, format='csr')
        H += -h * term
    return H

def get_ground_state(n_qubits, J, h):
    H = build_tfim_hamiltonian(n_qubits, J, h)
    energy, psi = eigsh(H, k=1, which='SA')
    psi = psi[:, 0]
    return psi / np.linalg.norm(psi), energy[0]


