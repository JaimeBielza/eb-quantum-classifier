def generate_entanglement_dataset(n_qubits, n_samples_per_class, seed=42):
    """
    Generate states where the two classes have similar probability 
    distributions but different entanglement structures.
    
    Class +1: States with bipartite entanglement (qubits 0-1 entangled,
              qubits 2-3 entangled, no cross entanglement)
    Class -1: States with global entanglement (all qubits entangled 
              through a chain)
    
    Measurement probabilities can be similar, but the correlation
    structure (and hence quantum fidelity patterns) differ.
    """
    rng = np.random.RandomState(seed)
    dim = 2 ** n_qubits
    
    states = []
    labels = []
    
    init_state = np.zeros(dim, dtype=complex)
    init_state[0] = 1.0
    
    def ry_gate(angle):
        return np.array([[np.cos(angle/2), -np.sin(angle/2)],
                        [np.sin(angle/2), np.cos(angle/2)]])
    
    def rz_gate(angle):
        return np.array([[np.exp(-1j*angle/2), 0],
                        [0, np.exp(1j*angle/2)]])
    
    def build_cnot(n_qubits, ctrl, targ):
        dim = 2 ** n_qubits
        cnot = np.zeros((dim, dim), dtype=complex)
        for basis in range(dim):
            ctrl_bit = (basis >> (n_qubits - 1 - ctrl)) & 1
            if ctrl_bit == 0:
                cnot[basis, basis] = 1.0
            else:
                new_basis = basis ^ (1 << (n_qubits - 1 - targ))
                cnot[new_basis, basis] = 1.0
        return cnot
    
    def full_ry(n_qubits, qubit, angle):
        gate = np.array([1.0])
        for i in range(n_qubits):
            gate = np.kron(gate, ry_gate(angle) if i == qubit else np.eye(2))
        return gate
    
    def full_rz(n_qubits, qubit, angle):
        gate = np.array([1.0])
        for i in range(n_qubits):
            gate = np.kron(gate, rz_gate(angle) if i == qubit else np.eye(2))
        return gate
    
    # Class +1: Bipartite entanglement (pairs)
    for _ in range(n_samples_per_class):
        angles = rng.uniform(0.3, 2.5, 8)
        
        U = np.eye(dim, dtype=complex)
        # Rotations
        for i in range(n_qubits):
            U = full_ry(n_qubits, i, angles[i]) @ U
        # Entangle pairs: 0-1 and 2-3 only
        U = build_cnot(n_qubits, 0, 1) @ U
        U = build_cnot(n_qubits, 2, 3) @ U
        # More rotations
        for i in range(n_qubits):
            U = full_rz(n_qubits, i, angles[i + n_qubits]) @ U
        # Another round of pair entanglement
        U = build_cnot(n_qubits, 1, 0) @ U
        U = build_cnot(n_qubits, 3, 2) @ U
        
        psi = U @ init_state
        psi = psi / np.linalg.norm(psi)
        states.append(psi)
        labels.append(+1)
    
    # Class -1: Chain entanglement (global)
    for _ in range(n_samples_per_class):
        angles = rng.uniform(0.3, 2.5, 8)
        
        U = np.eye(dim, dtype=complex)
        for i in range(n_qubits):
            U = full_ry(n_qubits, i, angles[i]) @ U
        # Entangle chain: 0-1, 1-2, 2-3
        U = build_cnot(n_qubits, 0, 1) @ U
        U = build_cnot(n_qubits, 1, 2) @ U
        U = build_cnot(n_qubits, 2, 3) @ U
        for i in range(n_qubits):
            U = full_rz(n_qubits, i, angles[i + n_qubits]) @ U
        # Reverse chain
        U = build_cnot(n_qubits, 3, 2) @ U
        U = build_cnot(n_qubits, 2, 1) @ U
        U = build_cnot(n_qubits, 1, 0) @ U
        
        psi = U @ init_state
        psi = psi / np.linalg.norm(psi)
        states.append(psi)
        labels.append(-1)
    
    return states, np.array(labels)


