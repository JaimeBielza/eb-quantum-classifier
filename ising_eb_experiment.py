def classical_feature_vector(psi):
    """Probability distribution â€” discards phase information."""
    return np.abs(psi) ** 2

def cosine_similarity(u, v):
    dot = np.dot(u, v)
    norm = np.linalg.norm(u) * np.linalg.norm(v)
    return dot / norm if norm > 1e-15 else 0.0

def rbf_similarity(u, v, gamma=1.0):
    return np.exp(-gamma * np.linalg.norm(u - v) ** 2)

def eb_classify(test_state, class_representatives, aggregation='max'):
    scores = {}
    for label, reps in class_representatives.items():
        class_pair_states = []
        for i, j in combinations(range(len(reps)), 2):
            class_pair_states.append(construct_pair_state(reps[i], reps[j]))
        if not class_pair_states:
            class_pair_states = [construct_pair_state(reps[0], reps[0])]
        
        fidelities = []
        for r in reps:
            test_rep = construct_pair_state(test_state, r)
            for cp in class_pair_states:
                fidelities.append(quantum_fidelity(test_rep, cp))
        
        fidelities = np.array(fidelities)
        if aggregation == 'max':
            scores[label] = np.max(fidelities)
        elif aggregation == 'mean':
            scores[label] = np.mean(fidelities)
        elif aggregation == 'median':
            scores[label] = np.median(fidelities)
    
    return max(scores, key=scores.get), scores

def cpc_classify(test_state, class_representatives, similarity='cosine',
                 aggregation='max', gamma=1.0):
    test_feat = classical_feature_vector(test_state)
    scores = {}
    
    for label, reps in class_representatives.items():
        rep_feats = [classical_feature_vector(r) for r in reps]
        class_pairs = []
        for i, j in combinations(range(len(rep_feats)), 2):
            class_pairs.append(np.concatenate([rep_feats[i], rep_feats[j]]))
        if not class_pairs:
            class_pairs = [np.concatenate([rep_feats[0], rep_feats[0]])]
        
        sims = []
        for rf in rep_feats:
            tr = np.concatenate([test_feat, rf])
            for cp in class_pairs:
                if similarity == 'cosine':
                    sims.append(cosine_similarity(tr, cp))
                else:
                    sims.append(rbf_similarity(tr, cp, gamma))
        
        sims = np.array(sims)
        if aggregation == 'max':
            scores[label] = np.max(sims)
        elif aggregation == 'mean':
            scores[label] = np.mean(sims)
        elif aggregation == 'median':
            scores[label] = np.median(sims)
    
    return max(scores, key=scores.get), scores

def build_fidelity_kernel(states):
    n = len(states)
    K = np.zeros((n, n))
    for i in range(n):
        for j in range(i, n):
            K[i, j] = K[j, i] = quantum_fidelity(states[i], states[j])
    return K

def build_fidelity_kernel_test(test_states, train_states):
    K = np.zeros((len(test_states), len(train_states)))
    for i in range(len(test_states)):
        for j in range(len(train_states)):
            K[i, j] = quantum_fidelity(test_states[i], train_states[j])
    return K

def select_representatives(states, n_reps, seed=42):
    rng = np.random.RandomState(seed)
    n = len(states)
    if n_reps >= n:
        return list(range(n))
    selected = [rng.randint(n)]
    for _ in range(n_reps - 1):
        min_fids = []
        for i in range(n):
            if i in selected:
                min_fids.append(np.inf)
            else:
                fids = [quantum_fidelity(states[i], states[s]) for s in selected]
                min_fids.append(max(fids))
        min_fids = np.array(min_fids)
        min_fids[list(selected)] = np.inf
        selected.append(np.argmin(min_fids))
    return selected


