def evaluate_dataset(dataset_name, states, labels, n_reps=5, n_splits=10):
    """Run full evaluation on a dataset."""
    
    print(f"\n{'=' * 75}")
    print(f"  DATASET: {dataset_name}")
    print(f"  Samples: {len(states)} | Classes: +1 ({np.sum(labels==1)}), -1 ({np.sum(labels==-1)})")
    print(f"  Representatives/class: {n_reps} | Folds: {n_splits}")
    print(f"{'=' * 75}")
    
    classical_features = np.array([classical_feature_vector(s) for s in states])
    
    # Check probability distribution overlap
    pos_probs = classical_features[labels == +1]
    neg_probs = classical_features[labels == -1]
    mean_diff = np.mean(np.abs(pos_probs.mean(axis=0) - neg_probs.mean(axis=0)))
    print(f"  Mean |Î”(prob)| between classes: {mean_diff:.6f}")
    print(f"  (Lower = harder for classical methods)")
    
    methods = ['EB-Max', 'EB-Mean', 'EB-Median',
               'CPC-Cosine-Max', 'CPC-RBF-Max',
               'QSVC', 'SVM (RBF)', 'KNN (k=3)', 'Random Forest', 'Logistic Reg.']
    
    results = {m: {k: [] for k in ['accuracy', 'precision', 'recall', 'f1', 'roc_auc']}
               for m in methods}
    
    skf = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=42)
    
    for fold, (train_idx, test_idx) in enumerate(skf.split(classical_features, labels)):
        train_states = [states[i] for i in train_idx]
        test_states_fold = [states[i] for i in test_idx]
        train_labels = labels[train_idx]
        test_labels = labels[test_idx]
        train_feats = classical_features[train_idx]
        test_feats = classical_features[test_idx]
        
        # Select representatives
        class_reps = {}
        for lbl in [+1, -1]:
            idx = np.where(train_labels == lbl)[0]
            cls_states = [train_states[i] for i in idx]
            rep_idx = select_representatives(cls_states, n_reps, seed=42+fold)
            class_reps[lbl] = [cls_states[i] for i in rep_idx]
        
        # EB variants
        for agg in ['max', 'mean', 'median']:
            name = f'EB-{agg.capitalize()}'
            preds, sc_list = [], []
            for ts in test_states_fold:
                pred, sc = eb_classify(ts, class_reps, agg)
                preds.append(pred)
                sc_list.append(sc[+1] - sc[-1])
            _record(results[name], test_labels, np.array(preds), np.array(sc_list))
        
        # CPC variants
        gamma = 1.0 / (2 * classical_features.shape[1])
        for sim, sim_name in [('cosine', 'Cosine'), ('rbf', 'RBF')]:
            name = f'CPC-{sim_name}-Max'
            preds, sc_list = [], []
            for ts in test_states_fold:
                pred, sc = cpc_classify(ts, class_reps, sim, 'max', gamma)
                preds.append(pred)
                sc_list.append(sc[+1] - sc[-1])
            _record(results[name], test_labels, np.array(preds), np.array(sc_list))
        
        # QSVC trained on the same representative subset used by EB/CPC
        qsvc_train_states = class_reps[+1] + class_reps[-1]
        qsvc_train_labels = np.array([+1] * len(class_reps[+1]) +
                                     [-1] * len(class_reps[-1]))
        K_tr = build_fidelity_kernel(qsvc_train_states)
        K_te = build_fidelity_kernel_test(test_states_fold, qsvc_train_states)
        svc = SVC(kernel='precomputed')
        svc.fit(K_tr, qsvc_train_labels)
        preds_q = svc.predict(K_te)
        dec_q = svc.decision_function(K_te)
        _record(results['QSVC'], test_labels, preds_q, dec_q)
        
        # Classical baselines
        for name, model in [
            ('SVM (RBF)', SVC(kernel='rbf')),
            ('KNN (k=3)', KNeighborsClassifier(n_neighbors=3)),
            ('Random Forest', RandomForestClassifier(n_estimators=100, random_state=42)),
            ('Logistic Reg.', LogisticRegression(max_iter=1000, random_state=42)),
        ]:
            model.fit(train_feats, train_labels)
            preds_cl = model.predict(test_feats)
            dec_cl = model.decision_function(test_feats) if hasattr(model, 'decision_function') \
                     else model.predict_proba(test_feats)[:, 1]
            _record(results[name], test_labels, preds_cl, dec_cl)
        
        if (fold + 1) % 5 == 0:
            print(f"  Fold {fold+1}/{n_splits} done")
    
    # Print results
    _print_results(dataset_name, results, methods)
    return results

def _record(storage, y_true, y_pred, y_score):
    storage['accuracy'].append(accuracy_score(y_true, y_pred))
    storage['precision'].append(precision_score(y_true, y_pred, pos_label=1, zero_division=0))
    storage['recall'].append(recall_score(y_true, y_pred, pos_label=1, zero_division=0))
    storage['f1'].append(f1_score(y_true, y_pred, pos_label=1, zero_division=0))
    try:
        storage['roc_auc'].append(roc_auc_score(y_true, y_score))
    except:
        storage['roc_auc'].append(0.5)

def _print_results(dataset_name, results, methods):
    print(f"\n  {'Algorithm':<22} {'Accuracy':>16} {'F1-score':>16} {'ROC-AUC':>16}")
    print(f"  {'-'*70}")
    
    sorted_m = sorted(methods, key=lambda x: np.mean(results[x]['roc_auc']), reverse=True)
    
    for name in sorted_m:
        acc_m, acc_s = np.mean(results[name]['accuracy']), np.std(results[name]['accuracy'])
        f1_m, f1_s = np.mean(results[name]['f1']), np.std(results[name]['f1'])
        auc_m, auc_s = np.mean(results[name]['roc_auc']), np.std(results[name]['roc_auc'])
        print(f"  {name:<22} {acc_m:.4f}Â±{acc_s:.4f}  {f1_m:.4f}Â±{f1_s:.4f}  {auc_m:.4f}Â±{auc_s:.4f}")
    
    # EB vs CPC analysis
    eb_auc = np.mean(results['EB-Max']['roc_auc'])
    cpc_cos = np.mean(results['CPC-Cosine-Max']['roc_auc'])
    cpc_rbf = np.mean(results['CPC-RBF-Max']['roc_auc'])
    best_cpc = max(cpc_cos, cpc_rbf)
