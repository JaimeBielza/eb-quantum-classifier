"""
=============================================================================
Quantum Data Experiment v2: Phase-Sensitive Classification
=============================================================================

The v1 experiment showed that with well-separated Ising phases, all methods
achieve perfect accuracy â€” the probability distributions alone suffice.

This v2 addresses that by creating scenarios where:
  (A) Samples are drawn NEAR the critical point, where probability 
      distributions overlap but quantum phases still differ
  (B) Random global phase rotations are applied, making |Ïˆ_i|Â² identical
      across transformed states while quantum fidelity still works
  (C) A "quantum circuit output" classification task where the data
      is generated by different parameterized circuits

These scenarios ensure that classical features (measurement probabilities)
are insufficient, and only methods with access to the full quantum state
(amplitudes + phases) can discriminate.
"""

