def generate_circuit_output_dataset(n_qubits, n_samples_per_class, seed=42):
    """
    Generate quantum states from two families of parameterized circuits.
    
    Class +1: Circuits with structure  R_Y(Î¸) Â· CNOT_chain Â· R_Z(Ï†)
              where Î¸ drawn from distribution A
    Class -1: Circuits with structure  R_Z(Ï†) Â· CNOT_chain Â· R_Y(Î¸)
              where Î¸ drawn from distribution B
    
    The two circuit families produce states with similar measurement
    distributions but different entanglement structures and phases.
    """
    rng = np.random.RandomState(seed)
    dim = 2 ** n_qubits
    
    states = []
    labels = []
    
    # Build CNOT chain matrix
    cnot_chain = np.eye(dim, dtype=complex)
    for i in range(n_qubits - 1):
        # CNOT with control=i, target=i+1
        cnot = np.zeros((dim, dim), dtype=complex)
        for basis in range(dim):
            ctrl_bit = (basis >> (n_qubits - 1 - i)) & 1
            if ctrl_bit == 0:
                cnot[basis, basis] = 1.0
            else:
                target_bit = (basis >> (n_qubits - 1 - (i+1))) & 1
                new_basis = basis ^ (1 << (n_qubits - 1 - (i+1)))
                cnot[new_basis, basis] = 1.0
        cnot_chain = cnot @ cnot_chain
    
    def ry_layer(angles):
        gate = np.array([1.0])
        for a in angles:
            ry = np.array([[np.cos(a/2), -np.sin(a/2)],
                          [np.sin(a/2), np.cos(a/2)]])
            gate = np.kron(gate, ry)
        return gate
    
    def rz_layer(angles):
        gate = np.array([1.0])
        for a in angles:
            rz = np.array([[np.exp(-1j*a/2), 0],
                          [0, np.exp(1j*a/2)]])
            gate = np.kron(gate, rz)
        return gate
    
    init_state = np.zeros(dim, dtype=complex)
    init_state[0] = 1.0
    
    # Class +1: R_Y Â· CNOT Â· R_Z Â· CNOT Â· R_Y
    for _ in range(n_samples_per_class):
        theta1 = rng.uniform(0.3, 0.9, n_qubits)
        phi = rng.uniform(0.5, 1.5, n_qubits)
        theta2 = rng.uniform(0.2, 0.7, n_qubits)
        
        U = ry_layer(theta2) @ cnot_chain @ rz_layer(phi) @ cnot_chain @ ry_layer(theta1)
        psi = U @ init_state
        psi = psi / np.linalg.norm(psi)
        states.append(psi)
        labels.append(+1)
    
    # Class -1: R_Z Â· CNOT Â· R_Y Â· CNOT Â· R_Z (reversed order)
    for _ in range(n_samples_per_class):
        phi1 = rng.uniform(1.5, 2.5, n_qubits)
        theta = rng.uniform(0.8, 1.8, n_qubits)
        phi2 = rng.uniform(1.0, 2.0, n_qubits)
        
        U = rz_layer(phi2) @ cnot_chain @ ry_layer(theta) @ cnot_chain @ rz_layer(phi1)
        psi = U @ init_state
        psi = psi / np.linalg.norm(psi)
        states.append(psi)
        labels.append(-1)
    
    return states, np.array(labels)


