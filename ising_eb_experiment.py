    delta = eb_auc - best_cpc
    
    best_cl_name = max(['SVM (RBF)', 'KNN (k=3)', 'Random Forest', 'Logistic Reg.'],
                       key=lambda x: np.mean(results[x]['roc_auc']))
    best_cl = np.mean(results[best_cl_name]['roc_auc'])
    
