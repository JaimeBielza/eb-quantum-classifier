    print(f"  Best classical:    {best_cl:.4f} ({best_cl_name})")
    print(f"  Î”(EB-classical):   {eb_auc - best_cl:+.4f}")
    
    advantage = "YES" if delta > 0.03 else ("Marginal" if delta > 0.01 else "NO")
    print(f"  Quantum advantage: {advantage}")


